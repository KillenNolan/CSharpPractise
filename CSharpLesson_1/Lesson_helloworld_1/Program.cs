using System;

namespace Lesson_helloworld_1
{
    class Program
    {
        static void Main(string[] args)
        {
            /*Console:控制台   写出一行字符
             WriteLine：写一行  显示
             写的内容：Hello WOrld!*/
            Console.WriteLine("Hello World!");

            /*控制台   读取一行字符   以回车键结束
             ReadLine:读一行
             将读一行的内容存在变量中
             */
            Console.ReadLine();
        }
    }
}
